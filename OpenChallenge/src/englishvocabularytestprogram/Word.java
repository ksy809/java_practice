package englishvocabularytestprogram;

public class Word {
	private final String key;
	private final String value;
	
	
	public Word(String key, String value) {
		super();
		this.key = key;
		this.value = value;
	}


	public String getKey() {
		return key;
	}


	public String getValue() {
		return value;
	}
	
	
}
